package lesson4;

public class MathOperation {

    public static void main(String[] args) {
        int one = 1;
        int two = 2;

        System.out.println(one + two);

        System.out.println(one - two);

        System.out.println(one / two);

        System.out.println(one * two);

        System.out.println(one % two);

        System.out.println(one < two);

        System.out.println(one > two);

        System.out.println(one == two);

        System.out.println(one <= two);

        System.out.println(one >= two);

        System.out.println(one != two);
    }
}
